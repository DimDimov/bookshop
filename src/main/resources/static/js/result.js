
function clickBestellung() {
    const font = document.getElementById("bestell");

    if (font.style.fontWeight === "normal") {
        font.style.fontWeight = "bold";

    }
    else
        font.style.fontWeight = "normal";
}

function clickDaten() {
    const font = document.getElementById("daten");

    if (font.style.fontWeight === "normal") {
        font.style.fontWeight = "bold";

    }
    else
        font.style.fontWeight = "normal";
}